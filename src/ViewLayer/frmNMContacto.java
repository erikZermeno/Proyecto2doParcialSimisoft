    public void llenarComboBoxLocalidades(){
        DefaultTableModel res = localidad.GetAllModelIDMunicipio();
        for (int i = 0; i < res.getRowCount(); i++) {
            comboLocalidad.addItem(String.valueOf(res.getValueAt(i, 1)));
            idLocalidad.add(Integer.parseInt(String.valueOf(res.getValueAt(i, 0))));
        }
    }
    
    public void llenarComboBoxMunicipios(){
        DefaultTableModel res = municipio.GetAllModelIDEstado();
        for (int i = 0; i < res.getRowCount(); i++) {
            comboMunicipios.addItem(String.valueOf(res.getValueAt(i, 1)));
            idMunicipio.add(Integer.parseInt(String.valueOf(res.getValueAt(i, 0))));
        }
    }
    
    public void llenarComboBoxEstados(){
        DefaultTableModel res = estado.GetAllModel();
        for (int i = 0; i < res.getRowCount(); i++) {
            comboEstado.addItem(String.valueOf(res.getValueAt(i, 1)));
            idEstado.add(Integer.parseInt(String.valueOf(res.getValueAt(i, 0))));
        }
    }
    
    public void esNumero (java.awt.event.KeyEvent evt, javax.swing.JTextField txt){
        char caracter = evt.getKeyChar();

        // Verificar si la tecla pulsada no es un digito
        if(((caracter < '0') ||
           (caracter > '9')) &&
           (caracter != '\b' /*corresponde a BACK_SPACE*/))
        {
           evt.consume();  // ignorar el evento de teclado
        }

    }
    
