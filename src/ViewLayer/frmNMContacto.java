    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtCodigoPostal.getText().isEmpty() || txtCalle.getText().isEmpty() || txtTelefono.getText().isEmpty() || txtComuna.getText().isEmpty() || idLocalidad.isEmpty() || idMunicipio.isEmpty() || idEstado.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Ingresa todos los campos");
        } else {
            
            if (idContacto == 0) {
                if (new Contactos(0, Integer.parseInt(txtCodigoPostal.getText()),
                        txtComuna.getText(),
                        txtCalle.getText(),
                        txtTelefono.getText(),
                        idEstado.get(comboEstado.getSelectedIndex()),
                        idMunicipio.get(comboMunicipios.getSelectedIndex()),
                        idLocalidad.get(comboLocalidad.getSelectedIndex())).Add()) {
                    JOptionPane.showMessageDialog(rootPane, "Contacto agregado correctamente!");
                    this.dispose();
                }
            } else {
                contacto.setCodigoPostal(Integer.parseInt(txtCodigoPostal.getText()));
                contacto.setComuna(txtComuna.getText());
                contacto.setCalle(txtCalle.getText());
                contacto.setTelefono(txtTelefono.getText());
                contacto.setIdEstado(idEstado.get(comboEstado.getSelectedIndex()));
                contacto.setIdMunicipio(idMunicipio.get(comboMunicipios.getSelectedIndex()));
                contacto.setIdLocalidad(idLocalidad.get(comboLocalidad.getSelectedIndex()));
                if (contacto.Update()) {
                    JOptionPane.showMessageDialog(rootPane, "Contacto actualizado correctamente!");
                    this.dispose();
                }

            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void txtTelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelefonoKeyTyped
        if (txtTelefono.getText().length() > 9) {
            evt.consume();
        } else {
            esNumero(evt, txtTelefono);
        }
    }//GEN-LAST:event_txtTelefonoKeyTyped

