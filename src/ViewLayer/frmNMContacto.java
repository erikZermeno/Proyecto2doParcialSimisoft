        txtCodigoPostal.setText(String.valueOf(contacto.getCodigoPostal()));
        txtComuna.setText(contacto.getComuna());
        txtCalle.setText(contacto.getCalle());
        txtTelefono.setText(contacto.getTelefono());
        
        //Llenado del combo box y seleccion
        idEstado.clear();
        comboEstado.removeAllItems();
        llenarComboBoxEstados();
