    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtNombre.getText().isEmpty() || txtTipo.getText().isEmpty() || txtUsuario.getText().isEmpty() || txtContrasena.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Ingresa todos los campos");
        } else {

            if (idUsuario == 0) {
                if (new Usuarios(0, idSucursal.get(comboSucursal.getSelectedIndex()),
                        idContacto.get(comboCalle.getSelectedIndex()),
                        txtTipo.getText(),
                        txtNombre.getText(),
                        txtUsuario.getText(),
                        txtContrasena.getText()).Add()) {
                    JOptionPane.showMessageDialog(rootPane, "Usuario agregado correctamente!");
                    this.dispose();
                }
            } else {
                usuario.setIdSucursal(idSucursal.get(comboSucursal.getSelectedIndex()));
                usuario.setIdContacto(idContacto.get(comboCalle.getSelectedIndex()));
                usuario.setTipo(txtTipo.getText());
                usuario.setNombre(txtNombre.getText());
                usuario.setUsuario(txtUsuario.getText());
                usuario.setPassword(txtContrasena.getText());
                if (usuario.Update()) {
                    JOptionPane.showMessageDialog(rootPane, "Usuario actualizado correctamente!");
                    this.dispose();
                }

            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

