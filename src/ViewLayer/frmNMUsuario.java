    public void llenarComboBoxContactos(){
        DefaultTableModel res = contacto.GetAllModel();
        for (int i = 0; i < res.getRowCount(); i++) {
            comboCalle.addItem(String.valueOf(res.getValueAt(i, 3)));
            idContacto.add(Integer.parseInt(String.valueOf(res.getValueAt(i, 0))));
        }
    }
    
    public void llenarComboBoxSucursal(){
        DefaultTableModel res = sucursal.GetAllModel();
        for (int i = 0; i < res.getRowCount(); i++) {
            comboSucursal.addItem(String.valueOf(res.getValueAt(i, 1)));
            idSucursal.add(Integer.parseInt(String.valueOf(res.getValueAt(i, 0))));
        }
    }
    
