    public void llenarComboBoxCategorias(){
        DefaultTableModel res = categoria.GetAllModel();
        for (int i = 0; i < res.getRowCount(); i++) {
            comboCategoria.addItem(String.valueOf(res.getValueAt(i, 1)));
            idCategoria.add(Integer.parseInt(String.valueOf(res.getValueAt(i, 0))));
        }
    }
    
    public void esDouble (java.awt.event.KeyEvent evt, javax.swing.JTextField txt){
        char caracter = evt.getKeyChar();

        // Verificar si la tecla pulsada no es un digito
        if(((caracter < '0') ||
           (caracter > '9')) &&
           (caracter != '\b' /*corresponde a BACK_SPACE*/)&&
           (caracter != '.'))
        {
           evt.consume();  // ignorar el evento de teclado
        }
        if(caracter == '.' && txt.getText().indexOf(".") != -1){
            evt.consume();
        }
    }
    
