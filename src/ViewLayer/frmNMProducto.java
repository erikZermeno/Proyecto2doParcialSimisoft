    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtNombre.getText().isEmpty() || txtPrecio.getText().isEmpty() || txtDescuento.getText().isEmpty() || txtCaducidad.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Ingresa todos los campos");
        } else {

            Date date = txtCaducidad.getDate();
            DateFormat f = new SimpleDateFormat("yyyy-MM-dd");
            String fecha = f.format(date);
            if (idProducto == 0) {
                if (new Productos(0, idCategoria.get(comboCategoria.getSelectedIndex()),
                        txtNombre.getText(),
                        Double.parseDouble(txtPrecio.getText()),
                        fecha,
                        Double.parseDouble(txtDescuento.getText())).Add()) {
                    JOptionPane.showMessageDialog(rootPane, "Producto agregado correctamente!");
                    this.dispose();
                }
            } else {
                producto.setIdCategoria(idCategoria.get(comboCategoria.getSelectedIndex()));
                producto.setNombre(txtNombre.getText());
                producto.setPrecio(Double.parseDouble(txtPrecio.getText()));
                producto.setCaducidad(fecha);
                producto.setDescuento(Double.parseDouble(txtDescuento.getText()));
                if (producto.Update()) {
                    JOptionPane.showMessageDialog(rootPane, "Producto actualizado correctamente!");
                    this.dispose();
                }

            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

