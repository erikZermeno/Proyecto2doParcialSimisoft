    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (tblProductosProveedores.getSelectedRow()>= 0) {
            int idProductoProveedor = (int) tblProductosProveedores.getValueAt(tblProductosProveedores.getSelectedRow(), 0);
            ProductosProveedores productoproveedor = new ProductosProveedores();
            productoproveedor.setIdProductoproveedor(idProductoProveedor);
            if(productoproveedor.Delete()){
                JOptionPane.showMessageDialog(rootPane, "Relacion eliminada correctamente");
            }else {
                JOptionPane.showMessageDialog(rootPane, "No ha sido posible eliminar la relacion");
            }
            tblProductosProveedores.setModel(productoproveedor.GetAllModel());
        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecciona una relacion");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

