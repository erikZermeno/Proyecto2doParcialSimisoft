    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (tblProductosProveedores.getSelectedRow()>= 0) {           
            int idProductoProveedor = (int) tblProductosProveedores.getValueAt(tblProductosProveedores.getSelectedRow(), 0);
            
            frmNMProductoProveedor obj;
            try {
                obj = new frmNMProductoProveedor(idProductoProveedor);
                obj.setTitle("Modificar Relacion");
                obj.setModal(true);
                obj.setVisible(true);
            } catch (ParseException ex) {
                Logger.getLogger(frmProductosProveedores.class.getName()).log(Level.SEVERE, null, ex);
            }

            tblProductosProveedores.setModel(new ProductosProveedores().GetAllModel());

        }else{
            JOptionPane.showMessageDialog(rootPane, "Selecciona una relacion");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

